#include <stdio.h>

// Now I will create and use some variables.


// main function
int main() {
    // x will be an integer with value 5
    int x = 5;

    // y will be an integer with value 7
    int y = 7;

    // print the value of x
    printf("x: %d\n", x);

    // print the value of y
    printf("y: %d\n", y);

    // print sum of x plus y
    printf("x + y: %d\n", x+y);

    // fnum will be a float
    float fnum = 3.14;

    // print fnum
    printf("fnum: %f\n", fnum);

    // print fnum plus x plus y
    printf("fnum + x + y: %f\n", x+y+fnum);

    // cletter is a character
    char cletter = 'B';

    // print cletter
    printf("cletter: %c\n", cletter);

    // change the value of x from 5 to 10
    x = 10;

    printf("x: %d\n", x);
    
}


